package com.importapp.registro.modelo;

public class ProductoSp {
    
    private String Nombre;

    private String Descripcion;

    private Integer TipoProducto;

    private Integer Moneda;

    private Double Valor;

    private Double Peso;

    private Integer TipoMedidaPeso;

    private Double DimensionAncho;

    private Double DimensionAlto;

    private Double DimensionFondo;

    private Integer TipoMedidaDimencion;

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getDescripcion() {
        return Descripcion;
    }

    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }

    public Integer getTipoProducto() {
        return TipoProducto;
    }

    public void setTipoProducto(Integer TipoProducto) {
        this.TipoProducto = TipoProducto;
    }

    public Integer getMoneda() {
        return Moneda;
    }

    public void setMoneda(Integer Moneda) {
        this.Moneda = Moneda;
    }

    public Double getValor() {
        return Valor;
    }

    public void setValor(Double Valor) {
        this.Valor = Valor;
    }

    public Double getPeso() {
        return Peso;
    }

    public void setPeso(Double Peso) {
        this.Peso = Peso;
    }

    public Integer getTipoMedidaPeso() {
        return TipoMedidaPeso;
    }

    public void setTipoMedidaPeso(Integer TipoMedidaPeso) {
        this.TipoMedidaPeso = TipoMedidaPeso;
    }

    public Double getDimensionAncho() {
        return DimensionAncho;
    }

    public void setDimensionAncho(Double DimensionAncho) {
        this.DimensionAncho = DimensionAncho;
    }

    public Double getDimensionAlto() {
        return DimensionAlto;
    }

    public void setDimensionAlto(Double DimensionAlto) {
        this.DimensionAlto = DimensionAlto;
    }

    public Double getDimensionFondo() {
        return DimensionFondo;
    }

    public void setDimensionFondo(Double DimensionFondo) {
        this.DimensionFondo = DimensionFondo;
    }

    public Integer getTipoMedidaDimencion() {
        return TipoMedidaDimencion;
    }

    public void setTipoMedidaDimencion(Integer TipoMedidaDimencion) {
        this.TipoMedidaDimencion = TipoMedidaDimencion;
    }

    
}
